    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // Bắt sự kiện nút Remove
        int []indexs = tableVocabulary.getSelectedRows();
          if(indexs.length == 0)//Nếu không chọn dòng nào
          {
              announcement("Must be have select a word to remove");
          }
          else
          {
            for(int i = 0; i < indexs.length; i++)
            {
                String en = (String) tableVocabulary.getValueAt(indexs[i], 0);
                listCtl.removeWord(en);
            }
            model.setRowCount(0);
            showtable();
            if(listCtl.checkIDperson(topicCtl.idperson))
            {
                announcement("Sucessfully");
            }
            else
            {          
                announcement("Your vocabulary list is empty");
            }
         }
    }//GEN-LAST:event_btnRemoveActionPerformed

