    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       // Bắt sự kiên nút Search
        if (txtTopicSearch.getText().isEmpty()) {
            announcement("Please enter topic's name");
        } else {
            if (tp.checkTopicName(txtTopicSearch.getText())) {
                model.setRowCount(0);
                showTable(txtTopicSearch.getText());
            } else {
                announcement("No result");
                txtTopicSearch.setText("");
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

