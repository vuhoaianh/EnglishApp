    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Bắt sự kiện nút add
        int[] indexs = topicTable.getSelectedRows();// vi tri cac dong word được chọn
        if (indexs.length == 0)
        {// Nếu chưa chọn
            announcement("Must have select at least a word");
        } else {
            
            int countSuccess = 0, countFalse = 0;
            for (int i = 0; i < indexs.length; i++)
            {
                String en = (String) topicTable.getValueAt(indexs[i], 0);
                if (new searchCtl().checkWordExits(topicCtl.idperson, en))
                {
                    countFalse++;
                } else 
                {
                    tp.addWordToPersonList(topicCtl.idperson, tp.getIDword(en));
                    countSuccess++;
                }
            }
            if (countSuccess == 0 && countFalse == 1)  // Mệt quá lười nghĩ thêm==> nghịch tí nên hơi nhiều trường hợp
            {
                announcement("The word is exits in your vocabulary");
            }
            else if (countSuccess == 0 && countFalse > 1) 
            {
                announcement("Add " + countFalse + " word failed");
            } 
            else if (countSuccess == 1 && countFalse == 0) 
            {
                announcement("Add a word sucessfully");
            }
            else if (countSuccess > 1 && countFalse == 0) 
            {
                announcement("Add " + countSuccess + " word sucessfully");
            } 
            else if (countSuccess == 1 && countFalse == 1)
            {
                announcement("Add a word sucessfully and a word failed");
            } else if (countSuccess == 1 && countFalse > 1)
            {
                announcement("Add a word sucessfully, " + countFalse + " words failed");
            } else if (countSuccess > 1 && countFalse == 1)
            {
                announcement("Add " + countSuccess + " words sucessfully, a word failed");
            } else
            {
                announcement("Add " + countSuccess + " words sucessfully, " + countFalse + " words failed");
            }
        }


    }//GEN-LAST:event_btnAddActionPerformed

